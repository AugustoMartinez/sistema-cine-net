    private void btnBajaSalaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBajaSalaMousePressed
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(null, "¬øDesea dar de baja la sala?", "Confirmaci√≥n", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION) {
            String nombre = listSalas.getSelectedItem().toString();
            for (int i = Cine.getListaSalas().size() - 1; i >= 0; i--) {
                Sala sala = Cine.getListaSalas().get(i);
                if (sala.getNombre().equals(nombre)) {
                    Cine.getListaSalas().remove(i);
                }
            }
            actualizarSalasNombre();
            actualizarSalas();
            Persistencia.actualizarSalas();
        }
    }//GEN-LAST:event_btnBajaSalaMousePressed

    private void actualizarListPeliculas() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        listPeliculas.setModel(model);
        for (Pelicula e : Cine.getListaPeliculas()) {
            if (!e.isBaja()) {
                model.addElement(e.getNombre());
            }
        }
    }

    private void actualizarGerentes() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        listGerentes.setModel(model);
        for (Usuario e : Cine.getListaUsuarios()) {
            if (e instanceof Gerente) {
                if (((Gerente) e).getIsAdmin() == true) {
                    model.addElement(e.getNombre() + " " + e.getApellido());
                }
            }
        }
    }

    private void actualizarSalas() {
        DefaultComboBoxModel<String> model2 = new DefaultComboBoxModel<>();
        listSalas.setModel(model2);
        for (Sala e : Cine.getListaSalas()) {
            if (!e.getBaja()) {
                model2.addElement(e.getNombre());
            }
        }
    }

    private void actualizarSalasNombre() {
        for (int i = Cine.getListaSalas().size() - 1; i >= 0; i--) {
            Sala sala = Cine.getListaSalas().get(i);
            sala.setNombre("Sala " + (i + 1));
            sala.setId(i + 1);
        }
        actualizarSalas();
        Persistencia.actualizarSalas();
    }

